t <- function(key) {
  lang <- if (exists("params") && !is.null(params$lang)) params$lang else "is"
  val  <- translations[[lang]][[key]]
  if (is.null(val)) {
    warning("Missing translation key: '", key, "' for lang='", lang, "'")
    key
  } else {
    val
  }
}
